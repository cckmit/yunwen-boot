package li.fyun.commons.core.utils;

public class YunwenException extends RuntimeException {

    public YunwenException() {
        super();
    }

    public YunwenException(String message) {
        super(message);
    }

    public YunwenException(String message, Throwable cause) {
        super(message, cause);
    }

    public YunwenException(Throwable cause) {
        super(cause);
    }

}
